	{
		$input = Jinput::all();

		$validation = new SOC\Validate\Answer($input);

		if($validation->passes()) {
			$answer = new SOC\Create\Answer($input);
			$answer->add();
			return 'Answer created!';
		} else {
			return dd($validation->getErrors());
		}
	}

